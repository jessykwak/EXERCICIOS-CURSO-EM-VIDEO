# OPERADORES
# adicao: +
# subtracao: -
# multiplicacao: *
# divisao: /
# potencia: **
# divisao inteira: //
# resto da divisao (modulo): %
# no python para comparacao utiliza-se 2 ==, no pascal 2 == eh pra resultado.

print(5 * 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 ** 2)
